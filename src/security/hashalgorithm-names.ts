/**
 * 定义了哈希算法名称枚举类型。
 * @enum
 * @public
 * @author Wang Yucai
 */
export enum HashAlgorithmNames {
  /**
   * 代表 MD5 哈希算法。
   */
  MD5 = "md5",
  /**
   * 代表 SHA1 哈希算法。
   */
  SHA1 = "sha1",
  /**
   * 代表 SHA256 哈希算法。
   */
  SHA256 = "sha256",
  /**
   * 代表 SHA512 哈希算法。
   */
  SHA512 = "sha512",
}
